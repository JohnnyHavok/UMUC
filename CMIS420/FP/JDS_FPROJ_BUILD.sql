(
  AGENTCODE NUMBER NOT NULL 
, AGENTID NUMBER NOT NULL 
, AGENTNAME VARCHAR2(20) NOT NULL 
, CONSTRAINT AGENT_PK PRIMARY KEY 
  (
    AGENTCODE 
  )
  ENABLE 
